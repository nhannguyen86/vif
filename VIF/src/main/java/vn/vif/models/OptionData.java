package vn.vif.models;

public class OptionData {
	
	private int id;
	
	private String name;
	
	public OptionData() {
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}